public class Pessoa
{
	private String nome;
	private long rg;
	
	/**
	 * @return the nome
	 */
	public String getNome()
	{
		return nome;
	}
	
	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome)
	{
		this.nome = nome;
	}
	
	/**
	 * @return the rg
	 */
	public long getRg()
	{
		return rg;
	}
	
	/**
	 * @param rg the rg to set
	 */
	public void setRg(long rg)
	{
		this.rg = rg;
	}
}
