public class TesteTiposPrimitivosCaractere
{

	/**
	 * @param args
	 */
	public static void main(String[] args)
	{
		char c = 'a';
		System.out.println("c = 'a' => c =" + c);
		
		char asc = 64;
		System.out.println("asc = 64 => asc = " + asc);
		
		char espaco = '\u0000';
		System.out.println("espaco = '\\u0000' => espaco = " + espaco);
		
		char carinha = '\u263A';
		System.out.println("carinha = '\\u263A' => carinha = " + carinha);
	}

}
